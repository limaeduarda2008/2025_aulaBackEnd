// if tradicional 
const aprovado = true;
let mensagem = " ";

if (aprovado == true) {
    mensagem = "Aprovado";
} else {
    mensagem = "Reprovado";
}

//----------------- estrutura do ternário ---------------------//

//------- condição ? valorSeVerdadeiro : valorSeFalso -------//

//---------------------------------------------------------//

mensagem = (aprovado == true) ? "Aprovado" : "Reprovado";



