// => é o “sinal” que transforma a função tradicional em arrow function.
// elas sempre são anônimas (não têm nome), mas você normalmente as guarda em uma variável.

//qual a estrutura dessa função
const somar = (v1, v2) => {
    const resultado = v1 + v2;
    return resultado;
};
console.log(somar(5, 3)); 

